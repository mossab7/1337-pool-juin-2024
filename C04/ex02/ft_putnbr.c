#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	temp;

	if (nb == -2147483648)
	{
		write(1, "-", 1);
		ft_putnbr(2147483648 / 10);
		ft_putnbr(8);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (nb / 10 != 0)
	{
		ft_putnbr(nb / 10);
	}
	temp = (nb % 10) + '0';
	write(1, &temp, 1);
}
