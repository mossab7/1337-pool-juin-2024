#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	root;

	root = 1;
	if (nb == 1)
		return (1);
	while (root < nb)
	{
		if (root * root == nb)
			return (root);
		root++;
	}
	return (0);
}
